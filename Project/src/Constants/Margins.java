package Constants;

import java.awt.Insets;

/**
 * This is a class that contains all the margins used in the UI.
 */
public class Margins {
    /**
     * This is the largest margin used for spacing tiles.
     */
    public static final Insets MAJOR_PANEL = new Insets(10, 20, 10, 10);
    /**
     * This is the space between panels in dashboard.
     */
    public static final Insets DASHBOARD = new Insets(5, 0, 5, 0);
    /**
     * This is the margin for information panel used in class routine page and bus schedule pages.
     */
    public static final Insets INFORMATION_PANEL = new Insets(0, 0, 0, 5);
    /**
     * This is the space between columns in multi-column pages.
     */
    public static final Insets COLUMN = new Insets(0, 5, 0, 5);
}
